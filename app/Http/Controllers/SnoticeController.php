        $userid=$id;
        $snotice = Snotice::where('userid',  $userid)
            ->get();


        if(count($snotice)>0)
        {

            return view('snotice.index')->with('user', $user)->with('snotice', $snotice[0]);
        }

        else {
            echo("no notice for u");
        }
